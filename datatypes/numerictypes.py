#integer
a=23
print(a)
print(type(a))

#float
b=20.7
print(b)
print(type(b))

#string
g="good day"
print(g)
print(type(g))

#complex
c=6j+7
print(c)
print(type(c))

#binary
d=0B1011
print(d)
print(type(d))

#boolean
print("2>3")
print(2>3)
print(type(2>3))

#type conversion->string to float
e=(float("20.5"))
print(e)

#type conversion->binary, hexadecimal, octagonal
f=bin(23)
print(f)
print(type(f))

h=hex(34)
print(h)

i=oct(33)
print(i)
